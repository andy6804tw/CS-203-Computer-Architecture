class CorrelatingPredictor extends Predictor {
    
    private static byte globalBranchState = 0;
    
    private int predictorState = 0;
    
    private static int BRANCH_TAKEN_MASK = 0x01;
    
    private TwoBitPredictor[] twoBitPredictors;
    
    public CorrelatingPredictor() {
        super();
        
        int predictorIndex;
        
        twoBitPredictors = new TwoBitPredictor[4];
        for(predictorIndex = 0;predictorIndex < 4;predictorIndex++){
            twoBitPredictors[predictorIndex] = new TwoBitPredictor();
        }
    }
    
    public void setPrediction(int param) {
        System.out.println("Correlating:setPrediction"); 
        twoBitPredictors[globalBranchState & 0x03].setPrediction(param);
    }
    
    public void setGlobalBranchState(boolean globalState) {
        globalBranchState <<= 1; 
        if(globalState == true){
            globalBranchState &= BRANCH_TAKEN_MASK;
        }
        System.out.println("Correlating:setGlobalBranchState");
    }
    
    public int getPrediction() {
        System.out.println("Correlating:getPrediction");
        return twoBitPredictors[globalBranchState & 0x03].getPrediction();
    }    
    
}

