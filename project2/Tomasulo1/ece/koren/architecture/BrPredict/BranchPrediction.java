    private void jList5MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jList5MouseClicked
        // Add your handling code here:
        java.awt.Image helpImage;
        int predictorType = jList5.getSelectedIndex();
        
        
        switch(predictorType){
            case 0:
                helpImage = getImage(getDocumentBase(),"OneBitPredictor.jpg");
                break;
            case 1:
                helpImage = getImage(getDocumentBase(),"TwoBitPredictor.jpg");
                
                break;
            case 2:
                helpImage = getImage(getDocumentBase(),"CorrelatingPredictor.jpg");
                
                break;
            default:
                helpImage = null;
              
                break;
        }
        helpPanel.setImage(helpImage);
        helpPanel.repaint();
        
    }//GEN-LAST:event_jList5MouseClicked

