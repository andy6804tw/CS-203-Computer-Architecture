    private void jComboBox59ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox59ItemStateChanged
        // Add your handling code here:
        int state,index;
        StringBuffer listItem = new StringBuffer();
        String immValue = new String();
        if(lastState != 0){
            return;
        }
        state = evt.getStateChange();
        if(state == evt.SELECTED){
            index = jComboBox59.getSelectedIndex();
            if(index  == 8){
                if(jComboBox4.getSelectedIndex() == 0){
                    jComboBox60.setEnabled(false);
                }
                lastState = state;
                jDialog1.setSize(200,200);
                jTextField9.setText("");
                jDialog1.show();
                immValue = jTextField9.getText();
                programe[14].setImmValue(Integer.parseInt(immValue));
                listItem.append("#");
                listItem.append(Integer.parseInt(immValue));
                jComboBox59.insertItemAt((String)listItem.toString(),index);
                jComboBox59.removeItemAt(index + 1);
                lastState = 0;
            }
            else{
                jComboBox60.setEnabled(true);
            }
        }        
    }//GEN-LAST:event_jComboBox59ItemStateChanged

