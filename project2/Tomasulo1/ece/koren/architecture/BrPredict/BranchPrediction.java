class Processor extends Thread {
    
    private Mutex syncLock;
    
    private BranchPrediction exec_context;
    
    public Processor(java.lang.String threadName,Mutex lock,BranchPrediction context) {
        super(threadName);
        syncLock = lock;
        exec_context = context;
    }
    
    public void run() {
        int pc,status;
        super.run();
        for(;;){
            syncLock.getLock();
            for(pc = 0; pc < exec_context.instructionCount;pc++){
                if((exec_context.programe[pc].instructionIndex > 5)&&(exec_context.programe[pc].instructionIndex < 9)){
                    exec_context.programe[pc].branchStatistic.show();
                }
            }
            //System.out.println("In run"+exec_context.instructionCount);
            for(pc = 0; pc < exec_context.instructionCount ;pc++){
                //System.out.println("instructionIndex,1 = "+instructionIndex);
                
                status = exec_context.programe[pc].execInstruction();
                //System.out.println("status = "+status);
                if(status < 1024){
                   pc = status - 1;
                   //System.out.println("instructionIndex,2 = "+instructionIndex);
                }
                else if(status == 9){
                    break;
                }
                try{
                    sleep(500);
                }
                catch(InterruptedException exception){
                  exception.printStackTrace();
                }
                exec_context.updateStatus();
            }
        }
    }
    
}

class Mutex {
    
    private int lock = 0;
    
    public synchronized void setLock() {
        lock = 1;
        notify();
    }
    
    public synchronized void getLock() {
        if(lock == 0){
            try{
                wait();
            }
            catch(InterruptedException exception){
                  exception.printStackTrace();
            }
        }
        lock = 0;
    }
}

